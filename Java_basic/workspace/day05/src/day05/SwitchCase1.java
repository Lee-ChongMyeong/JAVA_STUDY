package day05;

public class SwitchCase1 {
	public static void main(String[] args) {
		
		int a = 5;
		
		switch(a) {
		case 5:
			System.out.println("a는 5입니다.");
			break;
		case 6:
			System.out.println("a는 6입니다.");
			break;
		case 7:
			System.out.println("a는 7입니다.");
			break;
		default:
			System.out.println("a는 5,6,7이 아닙니다.");
			break;
		
		}
		
		
	}
}
