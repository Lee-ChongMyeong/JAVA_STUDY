package day04;

public class Operator8 {
	public static void main(String[] args) {
		
		int a = 13;
		
		a += 13;
		System.out.println("+= 13의 결과 : " +a );
		
		a -= 1;
		System.out.println(" == 1의 결과 : " + a);
		
		a *= 2;
		System.out.println(" *= 2의 결과 : " + a);
		
		a /= 10;
		System.out.println(" /= 10의 결과 : " + a);
		
		a %= 7;
		System.out.println(" %= 7의 결과 : " + a);
		
		
	}
}
