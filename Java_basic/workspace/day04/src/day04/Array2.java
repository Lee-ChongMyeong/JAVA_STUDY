package day04;

public class Array2 {
	public static void main(String[] args) {
		
		int arr[] = {1,2,3,4,5};
		// 배열 선언, 생성, 초기화를 한 줄에.
		
		System.out.println("arr내부의 요소 갯수 : " + arr.length);
		// 배열의 길이는 배열 변수명. length로 측정 가능
		
		
		System.out.println("arr내부의 0번째 요소 : " + arr[0]);
		System.out.println("arr내부의 1번째 요소 : " + arr[1]);
		System.out.println("arr내부의 2번째 요소 : " + arr[2]);
		System.out.println("arr내부의 3번째 요소 : " + arr[3]);
		System.out.println("arr내부의 4번째 요소 : " + arr[4]);
		// 선언, 생성, 초기화를 한번에 해도 같은 문법으로 호출 가능
		
		
		
	}
}
