package day03;

public class Operator1 {
	public static void main(String[] args) {
		
		int a = 3 + 5;
		int b = 7 - 2;
		int c = 4 * 6;
		int d = 5 / 2;
		int e = 12 % 4;
		double f = 5.0 / 2.0;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
		
	}
}
